#include "Triangle.h"


/*---------------------------------------------------------------------------*/
void Triangle::setPoints(const Real3& v1, const Real3& v2, const Real3& v3)
{
  m_p1.assign(v1);
  m_p2.assign(v2);
  m_p3.assign(v3);
}

/*---------------------------------------------------------------------------*/
Real3 Triangle::getPoint1() const
{
  return m_p1;
}

/*---------------------------------------------------------------------------*/
Real3 Triangle::getPoint2() const
{
  return m_p2;
}

/*---------------------------------------------------------------------------*/
Real3 Triangle::getPoint3() const
{
  return m_p3;
}

