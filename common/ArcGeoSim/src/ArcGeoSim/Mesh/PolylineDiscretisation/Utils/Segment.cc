#include "Segment.h"


/*---------------------------------------------------------------------------*/
void Segment::setPoints(const Real3& v1, const Real3& v2)
{
  m_p1.assign(v1);
  m_p2.assign(v2);
}

/*---------------------------------------------------------------------------*/
Real3 Segment::getPoint1() const
{
  return m_p1;
}

/*---------------------------------------------------------------------------*/
Real3 Segment::getPoint2() const
{
  return m_p2;
}

/*---------------------------------------------------------------------------*/
Real Segment::getP1X() const
{
  return m_p1.x;
}

/*---------------------------------------------------------------------------*/
Real Segment::getP1Y() const
{
  return m_p1.y;
}

/*---------------------------------------------------------------------------*/
Real Segment::getP1Z() const
{
  return m_p1.z;
}

/*---------------------------------------------------------------------------*/
Real Segment::getP2X() const
{
  return m_p2.x;
}

/*---------------------------------------------------------------------------*/
Real Segment::getP2Y() const
{
  return m_p2.y;
}

/*---------------------------------------------------------------------------*/
Real Segment::getP2Z() const
{
  return m_p2.z;
}
